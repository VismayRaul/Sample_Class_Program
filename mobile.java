public class mobile {
	private String name;
	private String colour;
	private int ip;
	
	public int getIp() {
		return ip;
	}
	
	public String getName() {
		return name;
	}
	
	public String getColour() {
		return colour;
	}
	
	public void setIp( int newIp) {
		ip = newIp;
	}
	
	public void setName(String newName) {
		name = newName;
	}
	
	public void setColour(String newColour) {
		colour = newColour;
	}
}
